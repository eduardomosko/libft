#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*mem;
	unsigned char	*end;
	unsigned char	target;

	mem = (unsigned char *) s;
	end = mem + n;
	target = c;
	while (mem != end)
		if (*mem++ == target)
			return (--mem);
	return (NULL);
}
